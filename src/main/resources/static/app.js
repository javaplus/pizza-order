var stompClient = null;

function setConnected(connected) {
    $("#connect").prop("disabled", connected);
    $("#disconnect").prop("disabled", !connected);
    if (connected) {
        $("#conversation").show();
    }
    else {
        $("#conversation").hide();
    }
    $("#greetings").html("");
}

function connect() {
    var socket = new SockJS('/orderflow-websocket');
    stompClient = Stomp.over(socket);
    stompClient.connect({}, function (frame) {
        setConnected(true);
        console.log('Connected: ' + frame);
        stompClient.subscribe('/topic/orders', function (order) {
            console.log("Got Messge!!!");
            console.log(order)
            showOrder(JSON.parse(order.body));
        });
    });
}

function disconnect() {
    if (stompClient !== null) {
        stompClient.disconnect();
    }
    setConnected(false);
    console.log("Disconnected");
}

function sendOrder() {
    // stompClient.send("/app/hello", {}, JSON.stringify({'name': $("#name").val()}));
    var orderForm = $( "form" ).serializeFormJSON();
    console.log(orderForm);
    $.ajax({
        type: "POST",
        url:"/orders",
        data: JSON.stringify(orderForm),
        contentType:"application/json; charset=utf-8",
        dataType:"json",
        success: function(data){
            alert("Order placed!" + data);
        }
    });
}

function showOrder(message) {
    console.log(message);
    $("#greetings").append("<tr><td>" + message.item + "</td></tr>");
}

function serializeFormJSON() {

    var o = {};
    var a = this.serializeArray();
    $.each(a, function () {
        if (o[this.name]) {
            if (!o[this.name].push) {
                o[this.name] = [o[this.name]];
            }
            o[this.name].push(this.value || '');
        } else {
            o[this.name] = this.value || '';
        }
    });
    return o;
}

$(function () {
    $.fn.serializeFormJSON = function () {

        var o = {};
        var a = this.serializeArray();
        $.each(a, function () {
            if (o[this.name]) {
                if (!o[this.name].push) {
                    o[this.name] = [o[this.name]];
                }
                o[this.name].push(this.value || '');
            } else {
                o[this.name] = this.value || '';
            }
        });
        return o;
    };
    $("form").on('submit', function (e) {
        e.preventDefault();
    });
    $( "#connect" ).click(function() { connect(); });
    $( "#disconnect" ).click(function() { disconnect(); });
    $( "#send" ).click(function() { sendOrder(); });
});